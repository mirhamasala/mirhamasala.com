export async function getStaticProps() {
  if (process.env.NODE_ENV === 'production') {
    await generateRssFeed()
  }

  return {
    props: {
      posts: (await getAllPosts())
        .slice(0, 4)
        .map(({ component, ...meta }) => meta),
    },
  }
}
